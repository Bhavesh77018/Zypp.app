// Generates an accurate, lightweight India SVG silhouette + projected city
// coordinates from real boundary data, written to src/data/indiaMap.ts.
//
//   node scripts/gen-india-path.mjs
//
// Re-run any time to regenerate. Source: geohacker/india (state boundaries).
import fs from "node:fs";
import path from "node:path";

const SRC =
  "https://raw.githubusercontent.com/geohacker/india/master/state/india_state.geojson";

const W = 440;
const H = 500;
const PAD = 16;
const TOL = 3; // px — point simplification tolerance
const MIN_RING = 6; // px — drop islands/fragments smaller than this

// Major operational cities [lat, lng] — projected with the same transform.
const CITIES = {
  "Delhi NCR": [28.6, 77.2],
  Jaipur: [26.9, 75.8],
  Kolkata: [22.57, 88.36],
  Mumbai: [19.07, 72.87],
  Pune: [18.52, 73.85],
  Hyderabad: [17.38, 78.48],
  Bengaluru: [12.97, 77.59],
  Chennai: [13.08, 80.27],
};

const gj = await fetch(SRC).then((r) => {
  if (r.status !== 200) throw new Error("HTTP " + r.status);
  return r.json();
});

const rings = [];
for (const f of gj.features) {
  const g = f.geometry;
  if (!g) continue;
  const polys =
    g.type === "Polygon"
      ? [g.coordinates]
      : g.type === "MultiPolygon"
        ? g.coordinates
        : [];
  for (const poly of polys) for (const ring of poly) rings.push(ring);
}

let aX = 1e9,
  aY = 1e9,
  bX = -1e9,
  bY = -1e9;
for (const r of rings)
  for (const [x, y] of r) {
    if (x < aX) aX = x;
    if (x > bX) bX = x;
    if (y < aY) aY = y;
    if (y > bY) bY = y;
  }

const sc = Math.min((W - 2 * PAD) / (bX - aX), (H - 2 * PAD) / (bY - aY));
const oX = (W - (bX - aX) * sc) / 2;
const oY = (H - (bY - aY) * sc) / 2;
const PX = (x) => Math.round(oX + (x - aX) * sc);
const PY = (y) => Math.round(oY + (bY - y) * sc); // invert Y (lat north = up)

let d = "";
let kept = 0;
for (const ring of rings) {
  const pts = ring.map((p) => [PX(p[0]), PY(p[1])]);
  let rx0 = 1e9,
    ry0 = 1e9,
    rx1 = -1e9,
    ry1 = -1e9;
  for (const [x, y] of pts) {
    if (x < rx0) rx0 = x;
    if (x > rx1) rx1 = x;
    if (y < ry0) ry0 = y;
    if (y > ry1) ry1 = y;
  }
  if (rx1 - rx0 < MIN_RING && ry1 - ry0 < MIN_RING) continue;
  let pX = null,
    pY = null,
    started = false,
    seg = "";
  for (const [X, Y] of pts) {
    if (pX !== null && Math.abs(X - pX) < TOL && Math.abs(Y - pY) < TOL) continue;
    seg += (started ? "L" : "M") + X + " " + Y + " ";
    started = true;
    pX = X;
    pY = Y;
  }
  if (seg) {
    d += seg + "Z ";
    kept++;
  }
}
d = d.trim();

const cityPx = Object.fromEntries(
  Object.entries(CITIES).map(([name, [lat, lng]]) => [name, [PX(lng), PY(lat)]]),
);

const out = `// AUTO-GENERATED by scripts/gen-india-path.mjs — do not edit by hand.
// Accurate India silhouette derived from real state-boundary GeoJSON,
// projected into a ${W}x${H} viewBox (equirectangular). Re-run the script to update.

export const INDIA_VIEWBOX = "0 0 ${W} ${H}";

export const INDIA_PATH =
  "${d}";

export interface MapCity { name: string; x: number; y: number; }

export const INDIA_CITIES: MapCity[] = [
${Object.entries(cityPx)
  .map(([name, [x, y]]) => `  { name: ${JSON.stringify(name)}, x: ${x}, y: ${y} },`)
  .join("\n")}
];
`;

const dest = path.join(process.cwd(), "src", "data", "indiaMap.ts");
fs.mkdirSync(path.dirname(dest), { recursive: true });
fs.writeFileSync(dest, out);
console.log(
  `Wrote ${dest}\n  rings kept: ${kept}\n  path length: ${d.length} chars`,
);
